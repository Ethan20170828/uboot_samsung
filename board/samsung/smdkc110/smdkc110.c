static inline void delay(unsigned long loops)
{
	__asm__ volatile ("1:\n" "subs %0, %1, #1\n" "bne 1b":"=r" (loops):"0"(loops));
}

/*
 * Miscellaneous platform dependent initialisations
 */
// 这个函数就是移植的关键
// 主要功能就是初始化DM9000网卡。
// 这个初始化过程和开发板上DM9000网卡芯片的硬件连接方式有关。必须要结合开发板原理图来分析，
// 然后决定这个函数怎么编程。
static void dm9000_pre_init(void)
{
	unsigned int tmp;
// 原来代码是三星工程师根据SMDKV210的硬件接法来写的程序，我们要根据自己的开发板的硬件接法去修改这个
// 程序，让网卡在我们的开发板上能工作。
// SROM_BW_REG和SROM_BC5_REG是我们s5pv210数据手册中的SROM的两个寄存器操作
#if defined(DM9000_16BIT_DATA)
// 可以看出三星开发板上的DM9000接在了bank5，是bit20-bit23
	//SROM_BW_REG &= ~(0xf << 20);
	//SROM_BW_REG |= (0<<23) | (0<<22) | (0<<21) | (1<<20);
// 我们开发板上的DM9000接在了bank1，是bit4-bit7					/* sxh changed */
	SROM_BW_REG &= ~(0xf << 4);
	SROM_BW_REG |= (1<<7) | (1<<6) | (1<<5) | (1<<4);
#else	
	SROM_BW_REG &= ~(0xf << 20);
	SROM_BW_REG |= (0<<19) | (0<<18) | (0<<16);
#endif
// 三星是BC5，我们这里改为BC1即可
	//SROM_BC5_REG = ((0<<28)|(1<<24)|(5<<16)|(1<<12)|(4<<8)|(6<<4)|(0<<0));
	SROM_BC1_REG = ((0<<28)|(1<<24)|(5<<16)|(1<<12)|(4<<8)|(6<<4)|(0<<0));

// MP01CON_REG寄存器是在s5pv210数据手册的GPIO中(MP01)
	tmp = MP01CON_REG;
	//tmp &=~(0xf<<20);
	//tmp |=(2<<20);
	tmp &=~(0xf<<4);
	tmp |=(2<<4);
	MP01CON_REG = tmp;
}


int board_init(void)
{
	DECLARE_GLOBAL_DATA_PTR;
#ifdef CONFIG_DRIVER_SMC911X
	smc9115_pre_init();
#endif

// 只要将网卡芯片正确的初始化了，则网卡芯片就能工作(意思是网卡驱动dm9000x.c和dm9000x.h依赖于这里的
// 初始化而工作)
// 在Linux系统中，网卡算是一个设备，这个设备的驱动工作后会生成有一个设备名叫ethn(n是0、1、2・・・)
// (无线网卡名字一般叫wlan0、wlan1・・・)。然后Linux系统用一些专用的命令来操作网卡，譬如ifconfig命令。
// 用Linux下的应用程序如何使用网卡驱动来进行网络通信? 最通用的方法就是socket接口。Linux系统中有一系列
// 的API和库函数，提供了一个socket编程接口，Linux下的应用程序都是通过socket来实现上网，socket内部就是
// 间接调用的网卡驱动实现网络通信的。
// Linux设计是非常完备的，应用层和驱动层是严格分离的。也就是说写网络编程应用层的人根本不用管驱动，
// 只要会用socket接口即可；写底层驱动的人根本不用管应用层，只要面向Linux的网络驱动模型即可。

// 一定要记住: uboot本身是一个裸机程序，是一个整体，没有分层。所以uboot中根本没有驱动和应用的概念。
// 按照逻辑来说，ping这样的命令实现的代码就是网络应用的应用程序，向dm9000x.c和dm9000x.h这样的代码
// 属于驱动程序。所以在uboot中这些东西是揉在一起的，应用是直接调用驱动实现的。也就是说ping命令内部
// 是直接调用了dm9000的网卡驱动中的函数来实现自己的。
#ifdef CONFIG_DRIVER_DM9000
	dm9000_pre_init();
#endif

	gd->bd->bi_arch_number = MACH_TYPE;
	gd->bd->bi_boot_params = (PHYS_SDRAM_1+0x100);

	return 0;
}
// 从运行信息以及bdinfo命令看到的结果，显示DRAM bank0和1的size值都设置错了。
// 使用md和mw命令测试内存，发现20000000和40000000
int dram_init(void)
{
	DECLARE_GLOBAL_DATA_PTR;

	gd->bd->bi_dram[0].start = PHYS_SDRAM_1;
	gd->bd->bi_dram[0].size = PHYS_SDRAM_1_SIZE;

#if defined(PHYS_SDRAM_2)
	gd->bd->bi_dram[1].start = PHYS_SDRAM_2;
	gd->bd->bi_dram[1].size = PHYS_SDRAM_2_SIZE;
#endif

	return 0;
}

